# frozen_string_literal: true

class MessageReaction < ApplicationRecord
  belongs_to :message
  belongs_to :user

  # Reaction types with pink icon styling
  REACTIONS = %w[like dislike love laugh sad].freeze

  # SVG icons for each reaction type (Heroicons outline style)
  REACTION_ICONS = {
